use chrono::prelude::*;

fn time_get() {
    let t = Local::now();
    let st = t.timestamp();
    let mt = st / 60;
    let ht = mt / 60;
    let dt = ht / 24;
    
    println!();
    println!("╔═══════════════════════════════════════════════════╗");
    println!("║  当前时间: {:<39}║", t.format("%Y-%m-%d %H:%M:%S"));
    println!("╠═══════════════════════════════════════════════════╣");
    println!("║  时间戳(秒): {:<12} | 十六进制: {:<12}║", st, format!("{:X}", st));
    println!("║  二进制: {:<41}║", format!("{:b}", st));
    println!("╠═══════════════════════════════════════════════════╣");
    println!("║  时间戳(分): {:<12} | 十六进制: {:<12}║", mt, format!("{:X}", mt));
    println!("║  二进制: {:<41}║", format!("{:b}", mt));
    println!("╠═══════════════════════════════════════════════════╣");
    println!("║  时间戳(时): {:<12} | 十六进制: {:<12}║", ht, format!("{:X}", ht));
    println!("║  二进制: {:<41}║", format!("{:b}", ht));
    println!("╠═══════════════════════════════════════════════════╣");
    println!("║  时间戳(天): {:<12} | 十六进制: {:<12}║", dt, format!("{:X}", dt));
    println!("║  二进制: {:<41}║", format!("{:b}", dt));
    println!("╚═══════════════════════════════════════════════════╝");
    println!();
}

fn main() {
    time_get();
}
